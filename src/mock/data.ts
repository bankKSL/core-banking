// ─── Repayment Schedule Generator ────────────────────────────
function generateAmortizationSchedule(
  loanApplicationId: string,
  principal: number,
  annualRate: number,
  tenureMonths: number,
  startDate: string,
  interestType: InterestType,
): RepaymentSchedule[] {
  const schedule: RepaymentSchedule[] = [];
  const monthlyRate = annualRate / 100 / 12;
  const start = new Date(startDate);
  let openingBalance = principal;

  if (interestType === "reducing_balance" || interestType === "fixed" || interestType === "floating") {
    const emi =
      monthlyRate === 0
        ? principal / tenureMonths
        : (principal * monthlyRate * Math.pow(1 + monthlyRate, tenureMonths)) /
          (Math.pow(1 + monthlyRate, tenureMonths) - 1);

    for (let i = 1; i <= tenureMonths; i++) {
      const interest = openingBalance * monthlyRate;
      const principalComp = emi - interest;
      const closingBalance = openingBalance - principalComp;
      const dueDate = new Date(start);
      dueDate.setMonth(start.getMonth() + i);

      schedule.push({
        id: `${loanApplicationId}-inst-${String(i).padStart(3, "0")}`,
        loanApplicationId,
        installmentNo: i,
        dueDate: dueDate.toISOString(),
        openingBalance: Math.round(openingBalance * 100) / 100,
        emi: Math.round(emi * 100) / 100,
        principalComponent: Math.round(principalComp * 100) / 100,
        interestComponent: Math.round(interest * 100) / 100,
        closingBalance: Math.round(closingBalance * 100) / 100,
        status: i <= 8 ? "paid" : "pending",
        paidDate: i <= 8 ? fmt(new Date(dueDate.getTime() - 2 * 86400000)) : undefined,
        paidAmount: i <= 8 ? Math.round(emi * 100) / 100 : undefined,
      });
      openingBalance = closingBalance;
    }
  } else if (interestType === "flat") {
    const totalInterest = ((principal * annualRate) / 100) * (tenureMonths / 12);
    const totalRepayable = principal + totalInterest;
    const flatEmi = totalRepayable / tenureMonths;

    for (let i = 1; i <= tenureMonths; i++) {
      const interestPerInstallment = totalInterest / tenureMonths;
      const principalPerInstallment = principal / tenureMonths;
      const closingBalance = openingBalance - principalPerInstallment;
      const dueDate = new Date(start);
      dueDate.setMonth(start.getMonth() + i);

      schedule.push({
        id: `${loanApplicationId}-inst-${String(i).padStart(3, "0")}`,
        loanApplicationId,
        installmentNo: i,
        dueDate: dueDate.toISOString(),
        openingBalance: Math.round(openingBalance * 100) / 100,
        emi: Math.round(flatEmi * 100) / 100,
        principalComponent: Math.round(principalPerInstallment * 100) / 100,
        interestComponent: Math.round(interestPerInstallment * 100) / 100,
        closingBalance: Math.round(closingBalance * 100) / 100,
        status: i <= 6 ? "paid" : "pending",
        paidDate: i <= 6 ? fmt(new Date(dueDate.getTime() - 1 * 86400000)) : undefined,
        paidAmount: i <= 6 ? Math.round(flatEmi * 100) / 100 : undefined,
      });
      openingBalance = closingBalance;
    }
  } else {
    const principalPerInstallment = principal / tenureMonths;
    for (let i = 1; i <= tenureMonths; i++) {
      const interest = openingBalance * monthlyRate;
      const emi = principalPerInstallment + interest;
      const closingBalance = openingBalance - principalPerInstallment;
      const dueDate = new Date(start);
      dueDate.setMonth(start.getMonth() + i);

      schedule.push({
        id: `${loanApplicationId}-inst-${String(i).padStart(3, "0")}`,
        loanApplicationId,
        installmentNo: i,
        dueDate: dueDate.toISOString(),
        openingBalance: Math.round(openingBalance * 100) / 100,
        emi: Math.round(emi * 100) / 100,
        principalComponent: Math.round(principalPerInstallment * 100) / 100,
        interestComponent: Math.round(interest * 100) / 100,
        closingBalance: Math.round(closingBalance * 100) / 100,
        status: i <= 5 ? "paid" : "pending",
        paidDate: i <= 5 ? fmt(new Date(dueDate.getTime() - 3 * 86400000)) : undefined,
        paidAmount: i <= 5 ? Math.round(emi * 100) / 100 : undefined,
      });
      openingBalance = closingBalance;
    }
  }
  return schedule;
}

